# Import module
import turtle

# Set up environment
turtle.setup(640,480) # Set up window size
turtle.showturtle()

# Show a message
turtle.write('Hello world.')

#Make turtle draw rectangle
turtle.forward(150)
turtle.right(90)
turtle.forward(150)
turtle.right(90)
turtle.forward(150)
turtle.right(90)
turtle.forward(150)
turtle.right(90)

# reset canvas
 


turtle.done()
